def get_league_rank(league, country):
    df = pd.read_csv('data/leagueRanking.csv')
    rank = df.loc[(df['League'] == league) & (df['Country'] == country), 'Rank']

    if rank.empty:
        return None
    else:
        return rank.values[0]

def add_league(response):

    if(len(get_league(response['league'])) == 0 ):
        try:

            league = response['league'].replace("\'", "''")
            country = response['country']

            conn = pymysql.connect(host='127.0.0.1', unix_socket='/tmp/mysql.sock', user='root',
                                   passwd='password', db='economics_of_sports')
            cur = conn.cursor()

            query = "INSERT INTO league (`name`,`country`, `national_rank`) VALUES ('%s', '%s', '%s');"
            final_query = query % (league, country, get_league_rank(league, country))

            cur.execute(final_query)
            conn.commit()
            cur.close()
            conn.close()
        except Exception as e:
            logging.error(e)






